    public int print(Graphics graf, PageFormat pageFor, int index) throws PrinterException 
    {
        if (index>0) 
        {
           return  NO_SUCH_PAGE;
        }
        Graphics2D imprimir = (Graphics2D) graf;
        imprimir.translate(pageFor.getImageableX()+30, pageFor.getImageableY()+30);
        imprimir.scale(0.85, 0.85);
        ImprimirFactura.printAll(graf);
        return PAGE_EXISTS;
    }
