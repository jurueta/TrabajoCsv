    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
    Metodos_Factura Datos = new Metodos_Factura(Integer.parseInt(txtNumeroFactura.getText()),
                                                txtFecha.getText(),
                                                Double.parseDouble(txtPrecio.getText()),
                                                Integer.parseInt(txtCantidad.getText()),
                                                txtArticulo.getText(),
                                                txtCodigoRefe.getText());
    
    DefaultTableModel modelo = (DefaultTableModel)DatosFactura.getModel();
        Object [] fila =  new Object[6]; 
        fila[0] = Datos.getNumeroFactura();
        fila[1] = Datos.getReferencia();
        fila[2] = Datos.getArticulo();
        fila[3] = Datos.getCantidad();
        fila[4] = Datos.getPrecio();
        fila[5] = String.valueOf(Datos.getPrecio()*Datos.getCantidad()); 
    modelo.addRow(fila);  
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
     if(DatosFactura.getSelectedRow()>=0) 
     {
     int fila =  DatosFactura.getSelectedRow();
     Double total = Integer.parseInt(txtCantidad.getText())*Double.parseDouble(txtPrecio.getText());
     DatosFactura.setValueAt(txtNumeroFactura.getText(), fila, 0);
     DatosFactura.setValueAt(txtCodigoRefe.getText(), fila, 1);
     DatosFactura.setValueAt(txtArticulo.getText(), fila, 2); 
     DatosFactura.setValueAt(txtCantidad.getText(), fila, 3);
     DatosFactura.setValueAt(txtPrecio.getText(), fila, 4);    
     DatosFactura.setValueAt(String.valueOf(total), fila, 5);
     }
      
     
     
    }//GEN-LAST:event_btnModificarActionPerformed

    private void DatosFacturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DatosFacturaMouseClicked
     if(DatosFactura.getSelectedRow()>=0)
     {   
         int fila =  DatosFactura.getSelectedRow();
         String[] modificar =   
         {
             String.valueOf(DatosFactura.getValueAt(fila,0)),
             String.valueOf(DatosFactura.getValueAt(fila, 1)),
             String.valueOf(DatosFactura.getValueAt(fila, 2)),
             String.valueOf(DatosFactura.getValueAt(fila, 3)),
             String.valueOf(DatosFactura.getValueAt(fila, 4)),
             String.valueOf(DatosFactura.getValueAt(fila, 5))
         };
             txtNumeroFactura.setText(modificar[0]);
             txtCodigoRefe.setText(modificar[1]);
             txtArticulo.setText(modificar[2]);
             txtCantidad.setText(modificar[3]);
             txtPrecio.setText(modificar[4]);
             lblResultado.setText(modificar[5]);
         
         
     }
    }//GEN-LAST:event_DatosFacturaMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
    DefaultTableModel modelo = (DefaultTableModel)DatosFactura.getModel(); 
    int fila =  DatosFactura.getSelectedRow();
    if(fila<0)
    {
        JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla para eliminarla");
    }
    else
    {
        int respuesta = JOptionPane.showConfirmDialog(null,"¿Esta seguro?","Alerta",JOptionPane.YES_NO_OPTION);
        if (JOptionPane.OK_OPTION == respuesta) {
            modelo.removeRow(fila);
        }
        
    }
    
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnImprimirFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirFacturaActionPerformed
     
      try
    {

       int resultado = JOptionPane.showConfirmDialog(null, "¿Desea imprimir el documento?", "Alerta", JOptionPane.YES_OPTION); 
       if (JOptionPane.OK_OPTION == resultado ) {
       PrinterJob formato = PrinterJob.getPrinterJob();
       formato.setPrintable(this);
       boolean verdadero = formato.printDialog();
      
       if (verdadero) 
        {
          
          formato.print();
        }   
    }
        }
            
    
    catch(PrinterException negativo)
    {
        JOptionPane.showMessageDialog(null, "Error en el programa","Error\n" +negativo , JOptionPane.INFORMATION_MESSAGE);
    }

    }//GEN-LAST:event_btnImprimirFacturaActionPerformed

